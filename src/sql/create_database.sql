CREATE TABLE Experiment (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    batch_name TEXT,
    sub_batch_name TEXT,
    file_name TEXT,
    file_path TEXT,
    igid TEXT,
    bfm TEXT,
    N INT NOT NULL,
    H INT NOT NULL,
    C INT NOT NULL,
    W INT NOT NULL,
    spatial_class TEXT,
    spatial_class_params JSON,
    value_classes_counts JSON,
    domain_class JSON,
    golden_range_min FLOAT,
    golden_range_max FLOAT,
    corrupted_values_count INT,
    corrupted_channels_count INT,
    layout TEXT,
    metadata JSON
);
